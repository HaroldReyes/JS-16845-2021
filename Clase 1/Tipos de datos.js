//Numeros enteros: numeros directos 10
//Numeros Decimales: numeros directos div por coma o punto ejem: 10.67
//Booleano: solo puede ser verdadero o false (true o false)

//Como declarar un dato
// 1) var
var texto = "hola mundo";
console.log("valor de la variable texto:", texto);
// 2) let
//Nueva palabra: Concatenar: unir texto o cualquier tipo de dato
let numeroYTexto = "mi edad es " + 30; //automaticamente retorna texto
console.log("valor de la variable numeroYTexto:", numeroYTexto);
// 3) cosnt
const soloNumero = 10 + 30; //automaticamente retorna un numero dado que numero mas numero es una operacion logico aritmetica
console.log("valor de la variable soloNumero:", soloNumero);
